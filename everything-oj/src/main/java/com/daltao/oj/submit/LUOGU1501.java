    public static class Task implements Runnable {
        final FastIO io;
        final Debug debug;
        int inf = (int) 1e8;

        public Task(FastIO io, Debug debug) {
            this.io = io;
            this.debug = debug;
        }

        @Override
        public void run() {
            solve();
        }

        public void solve() {
            int n = io.readInt();
            int q = io.readInt();

            LCTNode[] nodes = new LCTNode[n + 1];
            for (int i = 1; i <= n; i++) {
                nodes[i] = new LCTNode();
                nodes[i].val = 1;
                nodes[i].pushUp();
            }

            for (int i = 1; i < n; i++) {
                LCTNode a = nodes[io.readInt()];
                LCTNode b = nodes[io.readInt()];
                LCTNode.join(a, b);
            }

            for (int i = 0; i < q; i++) {
                switch (io.readChar()) {
                    case '+': {
                        LCTNode u = nodes[io.readInt()];
                        LCTNode v = nodes[io.readInt()];
                        int c = io.readInt();
                        LCTNode.findRoute(u, v);
                        LCTNode.splay(u);
                        u.add(c);
                    }
                    break;
                    case '-': {
                        LCTNode u1 = nodes[io.readInt()];
                        LCTNode v1 = nodes[io.readInt()];
                        LCTNode u2 = nodes[io.readInt()];
                        LCTNode v2 = nodes[io.readInt()];
                        LCTNode.cut(u1, v1);
                        LCTNode.join(u2, v2);
                    }
                    break;
                    case '*': {
                        LCTNode u = nodes[io.readInt()];
                        LCTNode v = nodes[io.readInt()];
                        int c = io.readInt();
                        LCTNode.findRoute(u, v);
                        LCTNode.splay(u);
                        u.mul(c);
                    }
                    break;
                    case '/': {
                        LCTNode u = nodes[io.readInt()];
                        LCTNode v = nodes[io.readInt()];
                        LCTNode.findRoute(u, v);
                        LCTNode.splay(u);
                        io.cache.append(u.sum).append('\n');
                    }
                    break;
                }
            }
        }
    }

