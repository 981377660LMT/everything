    public static class Task implements Runnable {
        final FastIO io;
        final Debug debug;
        int inf = (int) 1e8;

        Node[] nodes = new Node[128];
        Deque<String> trace = new ArrayDeque();

        {
            for (int i = 'a'; i <= 'z'; i++) {
                nodes[i] = new Node();
            }
        }

        public Task(FastIO io, Debug debug) {
            this.io = io;
            this.debug = debug;
        }

        @Override
        public void run() {
            int n = io.readInt();
            while (n-- > 0) {
                solve();
            }
        }

        public void dfs(Node root, String edge) {
            while (!root.edges.isEmpty()) {
                String tail = root.edges.remove(root.edges.size() - 1);
                dfs(nodes[tail.charAt(tail.length() - 1)], tail);
            }
            trace.addLast(edge);
        }

        Comparator<String> inverseComparator = new Comparator<String>() {
            @Override
            public int compare(String o1, String o2) {
                return o2.compareTo(o1);
            }
        };

        List<String> strings = new ArrayList();

        public void solve() {
            for (int i = 'a'; i <= 'z'; i++) {
                nodes[i].rank = 0;
                nodes[i].p = nodes[i];
                nodes[i].edges.clear();
                nodes[i].inDegree = 0;
            }
            trace.clear();
            strings.clear();

            int n = io.readInt();
            for (int i = 0; i < n; i++) {
                String s = io.readString();
                strings.add(s);
            }
            Collections.sort(strings, inverseComparator);

            for (String s : strings) {
                char head = s.charAt(0);
                char tail = s.charAt(s.length() - 1);
                nodes[head].edges.add(s);
                Node.union(nodes[head], nodes[tail]);
                nodes[tail].inDegree++;
            }

            Node set = null;
            int g1 = 0;
            int l1 = 0;
            boolean exist = true;
            for (int i = 'a'; i <= 'z'; i++) {
                if (nodes[i].edges.size() + nodes[i].inDegree == 0) {
                    continue;
                }
                if (set == null) {
                    set = nodes[i].find();
                }
                if (set != nodes[i].find()) {
                    exist = false;
                    break;
                }
                int d = nodes[i].edges.size() - nodes[i].inDegree;
                if (d == 1) {
                    g1++;
                } else if (d == -1) {
                    l1++;
                } else if (d != 0) {
                    exist = false;
                    break;
                }
            }

            exist = exist && g1 == l1 && g1 <= 1;
            if (!exist) {
                io.cache.append("***\n");
                return;
            }

            Node start = null;
            for (int i = 'a'; i <= 'z'; i++) {
                if (nodes[i].edges.size() > 0) {
                    start = nodes[i];
                    break;
                }
            }
            for (int i = 'a'; i <= 'z'; i++) {
                if (nodes[i].edges.size() > nodes[i].inDegree) {
                    start = nodes[i];
                    break;
                }
            }

            dfs(start, "");
            trace.removeLast();
            io.cache.append(trace.removeLast());
            while (!trace.isEmpty()) {
                io.cache.append('.').append(trace.removeLast());
            }
            io.cache.append('\n');
        }
    }

    public static class Node {
        Node p;
        int rank;
        List<String> edges = new ArrayList();
        int inDegree;

        Node find() {
            return p == p.p ? p : (p = p.find());
        }

        static void union(Node a, Node b) {
            a = a.find();
            b = b.find();
            if (a == b) {
                return;
            }
            if (a.rank == b.rank) {
                a.rank++;
            }
            if (a.rank > b.rank) {
                b.p = a;
            } else {
                a.p = b;
            }
        }
    }


    public static class FastIO {
        public final StringBuilder cache = new StringBuilder();
        private final InputStream is;
        private final OutputStream os;
        private final Charset charset;
        private StringBuilder defaultStringBuf = new StringBuilder(1 << 8);
        private byte[] buf = new byte[1 << 13];
        private int bufLen;
        private int bufOffset;
        private int next;

        public FastIO(InputStream is, OutputStream os, Charset charset) {
            this.is = is;
            this.os = os;
            this.charset = charset;
        }

        public FastIO(InputStream is, OutputStream os) {
            this(is, os, Charset.forName("ascii"));
        }

        private int read() {
            while (bufLen == bufOffset) {
                bufOffset = 0;
                try {
                    bufLen = is.read(buf);
                } catch (IOException e) {
                    throw new RuntimeException(e);
                }
                if (bufLen == -1) {
                    return -1;
                }
            }
            return buf[bufOffset++];
        }

        public void skipBlank() {
            while (next >= 0 && next <= 32) {
                next = read();
            }
        }

        public int readInt() {
            int sign = 1;

            skipBlank();
            if (next == '+' || next == '-') {
                sign = next == '+' ? 1 : -1;
                next = read();
            }

            int val = 0;
            if (sign == 1) {
                while (next >= '0' && next <= '9') {
                    val = val * 10 + next - '0';
                    next = read();
                }
            } else {
                while (next >= '0' && next <= '9') {
                    val = val * 10 - next + '0';
                    next = read();
                }
            }

            return val;
        }

        public long readLong() {
            int sign = 1;

            skipBlank();
            if (next == '+' || next == '-') {
                sign = next == '+' ? 1 : -1;
                next = read();
            }

            long val = 0;
            if (sign == 1) {
                while (next >= '0' && next <= '9') {
                    val = val * 10 + next - '0';
                    next = read();
                }
            } else {
                while (next >= '0' && next <= '9') {
                    val = val * 10 - next + '0';
                    next = read();
                }
            }

            return val;
        }

        public double readDouble() {
            boolean sign = true;
            skipBlank();
            if (next == '+' || next == '-') {
                sign = next == '+';
                next = read();
            }

            long val = 0;
            while (next >= '0' && next <= '9') {
                val = val * 10 + next - '0';
                next = read();
            }
            if (next != '.') {
                return sign ? val : -val;
            }
            next = read();
            long radix = 1;
            long point = 0;
            while (next >= '0' && next <= '9') {
                point = point * 10 + next - '0';
                radix = radix * 10;
                next = read();
            }
            double result = val + (double) point / radix;
            return sign ? result : -result;
        }

        public String readString(StringBuilder builder) {
            skipBlank();

            while (next > 32) {
                builder.append((char) next);
                next = read();
            }

            return builder.toString();
        }

        public String readString() {
            defaultStringBuf.setLength(0);
            return readString(defaultStringBuf);
        }

        public int readLine(char[] data, int offset) {
            int originalOffset = offset;
            while (next != -1 && next != '\n') {
                data[offset++] = (char) next;
                next = read();
            }
            return offset - originalOffset;
        }

        public int readString(char[] data, int offset) {
            skipBlank();

            int originalOffset = offset;
            while (next > 32) {
                data[offset++] = (char) next;
                next = read();
            }

            return offset - originalOffset;
        }

        public int readString(byte[] data, int offset) {
            skipBlank();

            int originalOffset = offset;
            while (next > 32) {
                data[offset++] = (byte) next;
                next = read();
            }

            return offset - originalOffset;
        }

        public char readChar() {
            skipBlank();
            char c = (char) next;
            next = read();
            return c;
        }

        public void flush() {
            try {
                os.write(cache.toString().getBytes(charset));
                os.flush();
                cache.setLength(0);
            } catch (IOException e) {
                throw new RuntimeException(e);
            }
        }

        public boolean hasMore() {
            skipBlank();
            return next != -1;
        }
    }

    public static class Debug {
        private boolean allowDebug;

        public Debug(boolean allowDebug) {
            this.allowDebug = allowDebug;
        }

        public void assertTrue(boolean flag) {
            if (!allowDebug) {
                return;
            }
            if (!flag) {
                fail();
            }
        }

        public void fail() {
            throw new RuntimeException();
        }

        public void assertFalse(boolean flag) {
            if (!allowDebug) {
                return;
            }
            if (flag) {
                fail();
            }
        }

        private void outputName(String name) {
            System.out.print(name + " = ");
        }

        public void debug(String name, int x) {
            if (!allowDebug) {
                return;
            }

            outputName(name);
            System.out.println("" + x);
        }

        public void debug(String name, long x) {
            if (!allowDebug) {
                return;
            }
            outputName(name);
            System.out.println("" + x);
        }

        public void debug(String name, double x) {
            if (!allowDebug) {
                return;
            }
            outputName(name);
            System.out.println("" + x);
        }

        public void debug(String name, int[] x) {
            if (!allowDebug) {
                return;
            }
            outputName(name);
            System.out.println(Arrays.toString(x));
        }

        public void debug(String name, long[] x) {
            if (!allowDebug) {
                return;
            }
            outputName(name);
            System.out.println(Arrays.toString(x));
        }

        public void debug(String name, double[] x) {
            if (!allowDebug) {
                return;
            }
            outputName(name);
            System.out.println(Arrays.toString(x));
        }

        public void debug(String name, Object x) {
            if (!allowDebug) {
                return;
            }
            outputName(name);
            System.out.println("" + x);
        }

        public void debug(String name, Object... x) {
            if (!allowDebug) {
                return;
            }
            outputName(name);
            System.out.println(Arrays.deepToString(x));
        }
    }
