    public static class Task implements Runnable {
        final FastIO io;
        final Debug debug;
        int inf = (int) 1e8;

        public Task(FastIO io, Debug debug) {
            this.io = io;
            this.debug = debug;
        }

        @Override
        public void run() {
            solve();
        }

        public void solve() {
            v = io.readInt();
            e = io.readInt();
            require = io.readInt();

            nodes = new Node[v];
            for (int i = 0; i < v; i++) {
                nodes[i] = new Node();
                nodes[i].id = i;
            }
            List<Edge>[] edges = new List[2];
            for (int i = 0; i < 2; i++) {
                edges[i] = new ArrayList<>(e);
            }
            for (int i = 0; i < e; i++) {
                Edge edge = new Edge();
                edge.a = nodes[io.readInt()];
                edge.b = nodes[io.readInt()];
                edge.dist = io.readInt();
                edges[io.readInt()].add(edge);
            }
            for (int i = 0; i < 2; i++) {
                edges[i].sort((a, b) -> a.dist - b.dist);
            }

            edgeWithColor0 = edges[0].toArray(new Edge[0]);
            edgeWithColor1 = edges[1].toArray(new Edge[0]);

            int l = -101;
            int r = 101;

            while (l < r) {
                int m = floorDiv(l + r, 2);
                mst(m);
                if (pick >= require) {
                    r = m;
                } else {
                    l = m + 1;
                }
            }
            mst(l);
            io.cache.append(totalFee + require * l);
        }

        public int floorDiv(int a, int b) {
            if (a >= 0) {
                return a / b;
            } else {
                return (a - b + 1) / b;
            }
        }

        int require;
        int v;
        int e;
        Node[] nodes;
        Edge[] edgeWithColor0;
        Edge[] edgeWithColor1;
        int totalFee;
        int pick;

        public void mst(int c) {
            for (int i = 0; i < v; i++) {
                nodes[i].rewind();
            }

            int tail0 = 0;
            int tail1 = 0;
            totalFee = 0;
            pick = 0;
            while (tail0 < edgeWithColor0.length || tail1 < edgeWithColor1.length) {
                if (tail1 >= edgeWithColor1.length ||
                        (tail0 < edgeWithColor0.length && edgeWithColor0[tail0].dist - c <=
                                edgeWithColor1[tail1].dist)) {
                    if (edgeWithColor0[tail0].a.find() != edgeWithColor0[tail0].b.find()) {
                        Node.merge(edgeWithColor0[tail0].a, edgeWithColor0[tail0].b);
                        totalFee += edgeWithColor0[tail0].dist - c;
                        pick++;
                    }
                    tail0++;
                } else {
                    if (edgeWithColor1[tail1].a.find() != edgeWithColor1[tail1].b.find()) {
                        Node.merge(edgeWithColor1[tail1].a, edgeWithColor1[tail1].b);
                        totalFee += edgeWithColor1[tail1].dist;
                    }
                    tail1++;
                }
            }
        }

    }

    public static class Edge {
        Node a;
        Node b;
        int dist;
    }

    public static class Node {
        Node p;
        int rank;
        int id;

        public void rewind() {
            p = this;
            rank = 0;
        }

        @Override
        public String toString() {
            return "" + id;
        }

        Node find() {
            return p.p == p ? p : (p = p.find());
        }

        static void merge(Node a, Node b) {
            a = a.find();
            b = b.find();
            if (a == b) {
                return;
            }
            if (a.rank == b.rank) {
                a.rank++;
            }
            if (a.rank > b.rank) {
                b.p = a;
            } else {
                a.p = b;
            }
        }
    }

    public static class FastIO {
        public final StringBuilder cache = new StringBuilder(1 << 13);
        private final InputStream is;
        private final OutputStream os;
        private final Charset charset;
        private StringBuilder defaultStringBuf = new StringBuilder(1 << 13);
        private byte[] buf = new byte[1 << 13];
        private int bufLen;
        private int bufOffset;
        private int next;

        public FastIO(InputStream is, OutputStream os, Charset charset) {
            this.is = is;
            this.os = os;
            this.charset = charset;
        }

        public FastIO(InputStream is, OutputStream os) {
            this(is, os, Charset.forName("ascii"));
        }

        private int read() {
            while (bufLen == bufOffset) {
                bufOffset = 0;
                try {
                    bufLen = is.read(buf);
                } catch (IOException e) {
                    throw new RuntimeException(e);
                }
                if (bufLen == -1) {
                    return -1;
                }
            }
            return buf[bufOffset++];
        }

        public void skipBlank() {
            while (next >= 0 && next <= 32) {
                next = read();
            }
        }

        public int readInt() {
            int sign = 1;

            skipBlank();
            if (next == '+' || next == '-') {
                sign = next == '+' ? 1 : -1;
                next = read();
            }

            int val = 0;
            if (sign == 1) {
                while (next >= '0' && next <= '9') {
                    val = val * 10 + next - '0';
                    next = read();
                }
            } else {
                while (next >= '0' && next <= '9') {
                    val = val * 10 - next + '0';
                    next = read();
                }
            }

            return val;
        }

        public long readLong() {
            int sign = 1;

            skipBlank();
            if (next == '+' || next == '-') {
                sign = next == '+' ? 1 : -1;
                next = read();
            }

            long val = 0;
            if (sign == 1) {
                while (next >= '0' && next <= '9') {
                    val = val * 10 + next - '0';
                    next = read();
                }
            } else {
                while (next >= '0' && next <= '9') {
                    val = val * 10 - next + '0';
                    next = read();
                }
            }

            return val;
        }

        public double readDouble() {
            boolean sign = true;
            skipBlank();
            if (next == '+' || next == '-') {
                sign = next == '+';
                next = read();
            }

            long val = 0;
            while (next >= '0' && next <= '9') {
                val = val * 10 + next - '0';
                next = read();
            }
            if (next != '.') {
                return sign ? val : -val;
            }
            next = read();
            long radix = 1;
            long point = 0;
            while (next >= '0' && next <= '9') {
                point = point * 10 + next - '0';
                radix = radix * 10;
                next = read();
            }
            double result = val + (double) point / radix;
            return sign ? result : -result;
        }

        public String readString(StringBuilder builder) {
            skipBlank();

            while (next > 32) {
                builder.append((char) next);
                next = read();
            }

            return builder.toString();
        }

        public String readString() {
            defaultStringBuf.setLength(0);
            return readString(defaultStringBuf);
        }

        public int readLine(char[] data, int offset) {
            int originalOffset = offset;
            while (next != -1 && next != '\n') {
                data[offset++] = (char) next;
                next = read();
            }
            return offset - originalOffset;
        }

        public int readString(char[] data, int offset) {
            skipBlank();

            int originalOffset = offset;
            while (next > 32) {
                data[offset++] = (char) next;
                next = read();
            }

            return offset - originalOffset;
        }

        public int readString(byte[] data, int offset) {
            skipBlank();

            int originalOffset = offset;
            while (next > 32) {
                data[offset++] = (byte) next;
                next = read();
            }

            return offset - originalOffset;
        }

        public char readChar() {
            skipBlank();
            char c = (char) next;
            next = read();
            return c;
        }

        public void flush() throws IOException {
            os.write(cache.toString().getBytes(charset));
            os.flush();
            cache.setLength(0);
        }

        public boolean hasMore() {
            skipBlank();
            return next != -1;
        }
    }

    public static class Debug {
        private boolean allowDebug;

        public Debug(boolean allowDebug) {
            this.allowDebug = allowDebug;
        }

        public void assertTrue(boolean flag) {
            if (!allowDebug) {
                return;
            }
            if (!flag) {
                fail();
            }
        }

        public void fail() {
            throw new RuntimeException();
        }

        public void assertFalse(boolean flag) {
            if (!allowDebug) {
                return;
            }
            if (flag) {
                fail();
            }
        }

        private void outputName(String name) {
            System.out.print(name + " = ");
        }

        public void debug(String name, int x) {
            if (!allowDebug) {
                return;
            }

            outputName(name);
            System.out.println("" + x);
        }

        public void debug(String name, long x) {
            if (!allowDebug) {
                return;
            }
            outputName(name);
            System.out.println("" + x);
        }

        public void debug(String name, double x) {
            if (!allowDebug) {
                return;
            }
            outputName(name);
            System.out.println("" + x);
        }

        public void debug(String name, int[] x) {
            if (!allowDebug) {
                return;
            }
            outputName(name);
            System.out.println(Arrays.toString(x));
        }

        public void debug(String name, long[] x) {
            if (!allowDebug) {
                return;
            }
            outputName(name);
            System.out.println(Arrays.toString(x));
        }

        public void debug(String name, double[] x) {
            if (!allowDebug) {
                return;
            }
            outputName(name);
            System.out.println(Arrays.toString(x));
        }

        public void debug(String name, Object x) {
            if (!allowDebug) {
                return;
            }
            outputName(name);
            System.out.println("" + x);
        }

        public void debug(String name, Object... x) {
            if (!allowDebug) {
                return;
            }
            outputName(name);
            System.out.println(Arrays.deepToString(x));
        }
    }
