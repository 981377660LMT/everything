        private boolean isPower2(int a, int b) {
            double x = Math.sqrt(a * a + b * b);
            int y = (int) (x + 0.5);
            return y * y == a * a + b * b;
        }

        public int optimize(Deque<Integer> deque) {
            int total = 0;
            if (deque.size() < 2) {
                return total;
            }
            int turn = random.nextInt(deque.size());
            while (turn-- > 0) {
                deque.addLast(deque.removeFirst());
            }
            if (random.nextBoolean()) {
                int time = deque.size();
                while (time-- > 0 && deque.size() >= 2) {
                    Integer first = deque.removeFirst();
                    if (deque.peekFirst() > first
                            && gcd.gcd(first, deque.peekFirst()) == 1 &&
                            isPower2(first, deque.peekFirst())) {
                        total += deque.removeFirst() + first;
                    } else {
                        deque.addLast(first);
                    }
                }
            } else {
                int time = deque.size();
                while (time-- > 0 && deque.size() >= 2) {
                    Integer first = deque.removeLast();
                    if (deque.peekLast() > first
                            && gcd.gcd(first, deque.peekLast()) == 1 &&
                            isPower2(first, deque.peekLast())) {
                        total += deque.removeLast() + first;
                    } else {
                        deque.addFirst(first);
                    }
                }
            }
        }

