    public static class Task implements Runnable {
        final FastIO io;
        final Debug debug;
        int inf = (int) 1e8;

        public Task(FastIO io, Debug debug) {
            this.io = io;
            this.debug = debug;
        }

        @Override
        public void run() {
            solve();
        }

        public void solve() {
            int n = io.readInt();
            int[] data = new int[1 + n];
            for (int i = 1; i <= n; i++) {
                data[i] = io.readInt();
            }
            DiscreteMap map = new DiscreteMap(data.clone(), 1, n + 1);
            List<Integer>[] indexes = new List[map.maxRank() + 1];
            for (int i = 0; i <= map.maxRank(); i++) {
                indexes[i] = new ArrayList<>();
            }
            for (int i = 1; i <= n; i++) {
                data[i] = map.rankOf(data[i]);
                indexes[data[i]].add(i);
            }
            PersistentSegment[] ps = new PersistentSegment[map.maxRank() + 1];
            ps[0] = PersistentSegment.NIL;
            for (int i = 1; i <= n; i++) {
                ps[0] = ps[0].clone();
                ps[0].update(i, i, 1, n, 1);
            }

            for (int i = 1; i <= map.maxRank(); i++) {
                ps[i] = ps[i - 1];
                for (int index : indexes[i - 1]) {
                    ps[i] = ps[i].clone();
                    ps[i].update(index, index, 1, n, -1);
                }
            }


            int q = io.readInt();
            int x = 0;
            int[] abcd = new int[4];
            int[] abs = new int[3];
            for (int i = 0; i < q; i++) {
                for (int j = 0; j < 4; j++) {
                    abcd[j] = (io.readInt() + x) % n + 1;
                }
                Arrays.sort(abcd);
                int a = abcd[0];
                int b = abcd[1];
                int c = abcd[2];
                int d = abcd[3];

                int l = map.minRank();
                int r = map.maxRank();
                while (l < r) {
                    int m = (l + r + 1) >> 1;

                    prepare(abs);
                    abs[1] = -inf;
                    ps[m].query(a, b, 1, n, abs);
                    int lCnt = abs[1];

                    prepare(abs);
                    ps[m].query(b + 1, c - 1, 1, n, abs);
                    int mCnt = abs[2];

                    prepare(abs);
                    abs[0] = -inf;
                    ps[m].query(c, d, 1, n, abs);
                    int rCnt = abs[0];

                    if (lCnt + mCnt + rCnt >= 0) {
                        l = m;
                    } else {
                        r = m - 1;
                    }
                }

                x = map.iThElement(l);
                io.cache.append(x).append('\n');
            }
        }

        public static void prepare(int[] abs) {
            abs[0] = abs[1] = abs[2] = 0;
        }
    }

