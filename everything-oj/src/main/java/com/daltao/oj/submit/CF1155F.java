    public static class Task implements Runnable {
        final FastIO io;
        final Debug debug;
        int inf = (int) 1e8;

        public Task(FastIO io, Debug debug) {
            this.io = io;
            this.debug = debug;
        }

        @Override
        public void run() {
            solve();
        }

        int order = 0;

        public int order() {
            return ++order;
        }

        Deque<Node> stack = new ArrayDeque<>();

        public void tarjan(Node root, Edge p) {
            if (root.dfn != 0) {
                return;
            }
            root.low = root.dfn = order();
            root.instk = true;
            stack.addLast(root);

            for (Edge edge : root.edgeList) {
                if (edge == p || edge.deleted) {
                    continue;
                }
                Node node = edge.another(root);
                tarjan(node, edge);
                if (node.instk) {
                    root.low = Math.min(root.low, node.low);
                }
            }

            if (root.low == root.dfn) {
                while (true) {
                    Node tail = stack.removeLast();
                    tail.set = root;
                    if (tail == root) {
                        break;
                    }
                }
            }
        }

        public void solve() {
            int n = io.readInt();
            int m = io.readInt();

            Node[] nodes = new Node[n + 1];
            Edge[] edges = new Edge[m + 1];
            for (int i = 1; i <= n; i++) {
                nodes[i] = new Node();
                nodes[i].id = i;
            }
            for (int i = 1; i <= m; i++) {
                Node a = nodes[io.readInt()];
                Node b = nodes[io.readInt()];
                Edge edge = new Edge();
                edge.a = a;
                edge.b = b;
                a.edgeList.add(edge);
                b.edgeList.add(edge);
                edges[i] = edge;
            }

            Set<Edge> remainEdge = new HashSet<>(Arrays.asList(edges).subList(1, m + 1));
            Set<Edge> building = new HashSet<>();

            for (int i = 0; i < 100; i++) {
                Randomized.randomizedArray(edges, 1, m);
                building.clear();
                building.addAll(Arrays.asList(edges).subList(1, m + 1));
                for (int j = 1; j <= m; j++) {
                    edges[j].deleted = false;
                }
                for (int j = 1; j <= m; j++) {
                    for (int k = 1; k <= n; k++) {
                        nodes[k].dfn = 0;
                    }
                    edges[j].deleted = true;
                    tarjan(nodes[1], null);
                    boolean inCycle = true;
                    for (int k = 1; k <= n; k++) {
                        if (nodes[k].set != nodes[1]) {
                            inCycle = false;
                            break;
                        }
                    }
                    if (!inCycle) {
                        edges[j].deleted = false;
                    } else {
                        building.remove(edges[j]);
                    }
                }
                if (building.size() < remainEdge.size()) {
                    Set<Edge> tmp = remainEdge;
                    remainEdge = building;
                    building = tmp;
                }
            }

            io.cache.append(remainEdge.size()).append('\n');
            for (Edge edge : remainEdge) {
                io.cache.append(edge.a.id).append(' ').append(edge.b.id).append('\n');
            }
        }
    }

