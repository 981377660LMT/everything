    public static class Task implements Runnable {
        final FastIO io;
        final Debug debug;
        int inf = (int) 1e8;

        public Task(FastIO io, Debug debug) {
            this.io = io;
            this.debug = debug;
        }

        @Override
        public void run() {
            solve();
        }

        int m;
        Node[] nodes;

        public void solve() {
            int n = io.readInt();
            m = io.readInt();
            nodes = new Node[m + 1];
            for (int i = 1; i <= m; i++) {
                nodes[i] = new Node();
            }
            int k = io.readInt();
            int e = io.readInt();

            for (int i = 0; i < e; i++) {
                Node a = nodes[io.readInt()];
                Node b = nodes[io.readInt()];
                Edge edge = new Edge();
                edge.a = a;
                edge.b = b;
                edge.len = io.readInt();
                a.next.add(edge);
                b.next.add(edge);
            }

            int d = io.readInt();
            for (int i = 0; i < d; i++) {
                int p = io.readInt();
                int a = io.readInt();
                int b = io.readInt();
                nodes[p].invalidPeriods.add(new Interval(a, b));
            }

            long[] dp = new long[n + 1];
            for (int i = 1; i <= n; i++) {
                dp[i] = inf;
                for (int j = 0; j < i; j++) {
                    dp[i] = Math.min(dp[i], dp[j] + (long)minLengthBetween1AndM(j + 1, i) * (i - j) + k);
                }
            }

            io.cache.append(dp[n] - k);
        }

        Deque<Node> deque = new ArrayDeque();

        public int minLengthBetween1AndM(int l, int r) {
            for (int i = 1; i <= m; i++) {
                nodes[i].dist = inf;
                nodes[i].inque = false;
                nodes[i].exist = nodes[i].exist(l, r);
            }
            nodes[1].dist = 0;
            nodes[1].inque = true;
            deque.add(nodes[1]);

            while (!deque.isEmpty()) {
                Node head = deque.removeFirst();
                head.inque = false;
                for (Edge edge : head.next) {
                    Node node = edge.another(head);
                    if (!node.exist) {
                        continue;
                    }
                    int dist = head.dist + edge.len;
                    if (dist >= node.dist) {
                        continue;
                    }
                    node.dist = dist;
                    if (node.inque) {
                        continue;
                    }
                    deque.addLast(node);
                    node.inque = true;
                }
            }

            return nodes[m].dist;
        }
    }

    public static class Edge {
        Node a;
        Node b;
        int len;

        Node another(Node x) {
            return a == x ? b : a;
        }
    }

    public static class Interval {
        final int l;
        final int r;

        public Interval(int l, int r) {
            this.l = l;
            this.r = r;
        }
    }

    public static class Node {
        List<Edge> next = new ArrayList(2);
        List<Interval> invalidPeriods = new ArrayList(2);
        boolean inque;
        int dist;
        boolean exist;

        public boolean exist(int l, int r) {
            for (Interval interval : invalidPeriods) {
                if (interval.r >= l && interval.l <= r) {
                    return false;
                }
            }
            return true;
        }
    }

    public static class FastIO {
        public final StringBuilder cache = new StringBuilder();
        private final InputStream is;
        private final OutputStream os;
        private final Charset charset;
        private StringBuilder defaultStringBuf = new StringBuilder(1 << 8);
        private byte[] buf = new byte[1 << 13];
        private int bufLen;
        private int bufOffset;
        private int next;

        public FastIO(InputStream is, OutputStream os, Charset charset) {
            this.is = is;
            this.os = os;
            this.charset = charset;
        }

        public FastIO(InputStream is, OutputStream os) {
            this(is, os, Charset.forName("ascii"));
        }

        private int read() {
            while (bufLen == bufOffset) {
                bufOffset = 0;
                try {
                    bufLen = is.read(buf);
                } catch (IOException e) {
                    throw new RuntimeException(e);
                }
                if (bufLen == -1) {
                    return -1;
                }
            }
            return buf[bufOffset++];
        }

        public void skipBlank() {
            while (next >= 0 && next <= 32) {
                next = read();
            }
        }

        public int readInt() {
            int sign = 1;

            skipBlank();
            if (next == '+' || next == '-') {
                sign = next == '+' ? 1 : -1;
                next = read();
            }

            int val = 0;
            if (sign == 1) {
                while (next >= '0' && next <= '9') {
                    val = val * 10 + next - '0';
                    next = read();
                }
            } else {
                while (next >= '0' && next <= '9') {
                    val = val * 10 - next + '0';
                    next = read();
                }
            }

            return val;
        }

        public long readLong() {
            int sign = 1;

            skipBlank();
            if (next == '+' || next == '-') {
                sign = next == '+' ? 1 : -1;
                next = read();
            }

            long val = 0;
            if (sign == 1) {
                while (next >= '0' && next <= '9') {
                    val = val * 10 + next - '0';
                    next = read();
                }
            } else {
                while (next >= '0' && next <= '9') {
                    val = val * 10 - next + '0';
                    next = read();
                }
            }

            return val;
        }

        public double readDouble() {
            boolean sign = true;
            skipBlank();
            if (next == '+' || next == '-') {
                sign = next == '+';
                next = read();
            }

            long val = 0;
            while (next >= '0' && next <= '9') {
                val = val * 10 + next - '0';
                next = read();
            }
            if (next != '.') {
                return sign ? val : -val;
            }
            next = read();
            long radix = 1;
            long point = 0;
            while (next >= '0' && next <= '9') {
                point = point * 10 + next - '0';
                radix = radix * 10;
                next = read();
            }
            double result = val + (double) point / radix;
            return sign ? result : -result;
        }

        public String readString(StringBuilder builder) {
            skipBlank();

            while (next > 32) {
                builder.append((char) next);
                next = read();
            }

            return builder.toString();
        }

        public String readString() {
            defaultStringBuf.setLength(0);
            return readString(defaultStringBuf);
        }

        public int readLine(char[] data, int offset) {
            int originalOffset = offset;
            while (next != -1 && next != '\n') {
                data[offset++] = (char) next;
                next = read();
            }
            return offset - originalOffset;
        }

        public int readString(char[] data, int offset) {
            skipBlank();

            int originalOffset = offset;
            while (next > 32) {
                data[offset++] = (char) next;
                next = read();
            }

            return offset - originalOffset;
        }

        public int readString(byte[] data, int offset) {
            skipBlank();

            int originalOffset = offset;
            while (next > 32) {
                data[offset++] = (byte) next;
                next = read();
            }

            return offset - originalOffset;
        }

        public char readChar() {
            skipBlank();
            char c = (char) next;
            next = read();
            return c;
        }

        public void flush() {
            try {
                os.write(cache.toString().getBytes(charset));
                os.flush();
                cache.setLength(0);
            } catch (IOException e) {
                throw new RuntimeException(e);
            }
        }

        public boolean hasMore() {
            skipBlank();
            return next != -1;
        }
    }

    public static class Debug {
        private boolean allowDebug;

        public Debug(boolean allowDebug) {
            this.allowDebug = allowDebug;
        }

        public void assertTrue(boolean flag) {
            if (!allowDebug) {
                return;
            }
            if (!flag) {
                fail();
            }
        }

        public void fail() {
            throw new RuntimeException();
        }

        public void assertFalse(boolean flag) {
            if (!allowDebug) {
                return;
            }
            if (flag) {
                fail();
            }
        }

        private void outputName(String name) {
            System.out.print(name + " = ");
        }

        public void debug(String name, int x) {
            if (!allowDebug) {
                return;
            }

            outputName(name);
            System.out.println("" + x);
        }

        public void debug(String name, long x) {
            if (!allowDebug) {
                return;
            }
            outputName(name);
            System.out.println("" + x);
        }

        public void debug(String name, double x) {
            if (!allowDebug) {
                return;
            }
            outputName(name);
            System.out.println("" + x);
        }

        public void debug(String name, int[] x) {
            if (!allowDebug) {
                return;
            }
            outputName(name);
            System.out.println(Arrays.toString(x));
        }

        public void debug(String name, long[] x) {
            if (!allowDebug) {
                return;
            }
            outputName(name);
            System.out.println(Arrays.toString(x));
        }

        public void debug(String name, double[] x) {
            if (!allowDebug) {
                return;
            }
            outputName(name);
            System.out.println(Arrays.toString(x));
        }

        public void debug(String name, Object x) {
            if (!allowDebug) {
                return;
            }
            outputName(name);
            System.out.println("" + x);
        }

        public void debug(String name, Object... x) {
            if (!allowDebug) {
                return;
            }
            outputName(name);
            System.out.println(Arrays.deepToString(x));
        }
    }
