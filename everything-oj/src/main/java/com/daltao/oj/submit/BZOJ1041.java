        public void solve() {
            long r = io.readLong();
            long rr = r + r;
            if (r == 0) {
                io.cache.append(1);
                return;
            }

            List<Long> factors = new ArrayList();
            for (long i = 1; i * i <= rr; i++) {
                if (rr % i != 0) {
                    continue;
                }
                factors.add(i);
                if (i * i != rr) {
                    factors.add(rr / i);
                }
            }

            long total = 0;
            for (Long d : factors) {
                for (long a = 1; ; a++) {
                    long a2 = a * a;
                    long b2 = rr / d - a2;
                    if (a2 >= b2) {
                        break;
                    }
                    if (gcd.gcd(a2, b2) != 1) {
                        continue;
                    }
                    long y2 = d * d * a2 * b2;
                    long x2 = r * r - y2;
                    if (!(isPow2(b2) && isPow2(x2))) {
                        continue;
                    }
                    if (x2 <= 0 || x2 >= r * r) {
                        continue;
                    }
                    if ((r - d * a2) * (r - d * a2) != x2) {
                        continue;
                    }
                    total++;
                }
            }

            total = (total + 1) * 4;
            io.cache.append(total);
        }

        public long bruteForce(long r) {
            //long r = io.readInt();
            long total = 0;
            for (int i = 1; i < r; i++) {
                if (isPow2(r * r - (long) i * i)) {
                    total++;
                }
            }

            io.cache.append(total * 4 + 4);
            return total * 4 + 4;
        }

        public boolean isPow2(long x) {
            long l = 1;
            long r = x;
            while (l < r) {
                long m = (l + r + 1) >> 1;
                if (m * m <= x) {
                    l = m;
                } else {
                    r = m - 1;
                }
            }
            return l * l == x;
        }
