    public static class Task implements Runnable {
        final FastIO io;
        final Debug debug;
        int inf = (int) 1e8;

        public Task(FastIO io, Debug debug) {
            this.io = io;
            this.debug = debug;
        }

        @Override
        public void run() {
            solve();
        }

        St<Node> st;

        public Node lca(Node a, Node b) {
            if (a.dfn > b.dfn) {
                Node tmp = a;
                a = b;
                b = tmp;
            }
            return st.query(a.dfn, b.dfn);
        }

        public void solve() {
            int n = io.readInt();
            int m = io.readInt();
            Node[] nodes = new Node[n + 1];
            for (int i = 1; i <= n; i++) {
                nodes[i] = new Node();
                nodes[i].id = i;
            }
            for (int i = 1; i < n; i++) {
                Node a = nodes[io.readInt()];
                Node b = nodes[io.readInt()];
                a.next.add(b);
                b.next.add(a);
            }

            List<Node> trace = new ArrayList(2 * n);
            dfs(nodes[1], null, 0, trace);
            st = new St(trace.toArray(), trace.size(), new Comparator<Node>() {
                @Override
                public int compare(Node o1, Node o2) {
                    return o1.dfn - o2.dfn;
                }
            });

            Node[] waitPoints = new Node[3];
            for (int i = 0; i < m; i++) {
                Node a = nodes[io.readInt()];
                Node b = nodes[io.readInt()];
                Node c = nodes[io.readInt()];
                Node ab = lca(a, b);
                Node bc = lca(b, c);
                Node ac = lca(a, c);
                waitPoints[0] = a;
                waitPoints[1] = b;
                waitPoints[2] = c;

                if (bc.depth >= ab.depth && bc.depth >= ac.depth) {
                    Memory.swap(waitPoints, 0, 2);
                } else if (ac.depth >= ab.depth && ac.depth >= bc.depth) {
                    Memory.swap(waitPoints, 1, 2);
                }
                a = waitPoints[0];
                b = waitPoints[1];
                c = waitPoints[2];
                ab = lca(a, b);
                bc = lca(b, c);
                ac = lca(a, c);

                int cost = a.depth + b.depth - ab.depth * 2
                        + c.depth - ac.depth + ab.depth - ac.depth;
                io.cache.append(ab.id).append(' ').append(cost).append('\n');
            }
        }

        public static void dfs(Node root, Node father, int depth, List<Node> trace) {
            root.depth = depth;
            root.dfn = trace.size();
            trace.add(root);
            for (Node node : root.next) {
                if (node == father) {
                    continue;
                }
                dfs(node, root, depth + 1, trace);
                trace.add(root);
            }
        }
    }

    public static class Memory {
        public static <T> void swap(T[] data, int i, int j) {
            T tmp = data[i];
            data[i] = data[j];
            data[j] = tmp;
        }

        public static void swap(char[] data, int i, int j) {
            char tmp = data[i];
            data[i] = data[j];
            data[j] = tmp;
        }

        public static void swap(int[] data, int i, int j) {
            int tmp = data[i];
            data[i] = data[j];
            data[j] = tmp;
        }

        public static void swap(long[] data, int i, int j) {
            long tmp = data[i];
            data[i] = data[j];
            data[j] = tmp;
        }

        public static void swap(double[] data, int i, int j) {
            double tmp = data[i];
            data[i] = data[j];
            data[j] = tmp;
        }

        public static <T> int min(T[] data, int from, int to, Comparator<T> cmp) {
            int m = from;
            for (int i = from + 1; i < to; i++) {
                if (cmp.compare(data[m], data[i]) > 0) {
                    m = i;
                }
            }
            return m;
        }

        public static <T> void move(T[] data, int from, int to, int step) {
            int len = to - from;
            step = len - (step % len + len) % len;
            Object[] buf = new Object[len];
            for (int i = 0; i < len; i++) {
                buf[i] = data[(i + step) % len + from];
            }
            System.arraycopy(buf, 0, data, from, len);
        }

        public static <T> void reverse(T[] data, int f, int t) {
            int l = f, r = t - 1;
            while (l < r) {
                swap(data, l, r);
                l++;
                r--;
            }
        }

        public static void reverse(int[] data, int f, int t) {
            int l = f, r = t - 1;
            while (l < r) {
                swap(data, l, r);
                l++;
                r--;
            }
        }

        public static void copy(Object[] src, Object[] dst, int srcf, int dstf, int len) {
            if (len < 8) {
                for (int i = 0; i < len; i++) {
                    dst[dstf + i] = src[srcf + i];
                }
            } else {
                System.arraycopy(src, srcf, dst, dstf, len);
            }
        }

        public static void fill(int[][] x, int val) {
            for (int[] v : x) {
                Arrays.fill(v, val);
            }
        }

        public static void fill(int[][][] x, int val) {
            for (int[][] v : x) {
                fill(v, val);
            }
        }
    }


